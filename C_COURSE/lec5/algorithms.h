
/**
 * @brief The function search if given number exists in array
 * @param arr: Array of numbers
 * @param length: Size of array
 * @param number: Number to search in array
 * @return index of number in the array
 *      (return value = length if number does not exist)
*/
int LinearSearch (int arr[], int length, int number);

/**
 * @brief The function search if given number exists in array
 * @param arr: Array of numbers
 * @param length: Size of array
 * @param number: Number to search in array
 * @return index of number in the array
 *      (return value = length if number does not exist)
*/
int BinarySearch (int arr[], int length, int number);

/**
 * @brief The function sorts given array (Ascending)
 * @param arr: Array of numbers
 * @param length: Size of array
*/
void BubbleSort (int arr[], int length);

